    if failed:
        warning_lines.append(f"- {failed} planner runs failed; inspect step11a_solver_diagnostics.csv.")
    if overlays:
        warning_lines.append(f"- {int(overlays)} enriched comparisons have high trajectory overlap with baseline.")
