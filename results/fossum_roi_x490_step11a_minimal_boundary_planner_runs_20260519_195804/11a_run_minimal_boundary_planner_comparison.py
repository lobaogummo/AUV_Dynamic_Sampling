        f"- {r.case_id} / {r.formulation}: {r.status}, waypoints={r.number_of_waypoints}, length={r.trajectory_length_km:.3f} km, collected boundary={r.collected_boundary_score:.3f}, collected STD={r.collected_STD_score:.3f}"
